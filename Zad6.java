package zadania;

public class Zad6 {

	public static void main(String[] args) {

		char znak = 'a';
		int wrt = 8;
		float fl = 10.0f;
		double db = 13.4;
		byte bt = 2;
		
		System.out.println(znak + wrt);
		//char + int = int
		
		System.out.println(wrt + znak);
		//int + char = int
		
		System.out.println(fl + db);
		//float + double = double
		
		System.out.println(bt + wrt);
		//byte + int = int
	}

}
